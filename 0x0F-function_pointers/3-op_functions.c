#include "3-calc.h"
#include <stdlib.h>
#include <stdio.h>
/**
* op_add - sum fun
*
* @a: int
* @b: int
*
* Return: int (success)
*/
int op_add(int a, int b)
{
return (a + b);
}
/**
* op_sub - sub fun
*
* @a: int
* @b: int
*
* Return: int (success)
*/
int op_sub(int a, int b)
{
return (a - b);
}
/**
* op_mul - mul fun
*
* @a: int
* @b: int
*
* Return: int (success)
*/
int op_mul(int a, int b)
{
return (a * b);
}
/**
* op_div - div fun
*
* @a: int
* @b: int
*
* Return: int (success)
*/
int op_div(int a, int b)
{
return (a / b);
}
/**
* op_mod - mod fun
*
* @a: int
* @b: int
*
* Return: int (success)
*/
int op_mod(int a, int b)
{
return (a % b);
}
